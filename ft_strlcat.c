#include "libft.h"

size_t	ft_strlcat(char *dst, char *src, size_t dstsize)
{
	int		i;
	size_t	dst_len;

	dst_len = ft_strlen(dst);
	if ((dstsize) <= dst_len)
		return (dstsize + ft_strlen(src));
	i = 0;
	while (src[i] && (dst_len + i) < (dstsize - 1))
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	return (dst_len + ft_strlen(src));
}
